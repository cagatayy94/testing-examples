package io.cagatay;

public class Calculator {
    int addition(int numberOne, int numberTwo){
        return numberOne+numberTwo;
    }

    double division(double dividend, double divider){
        return dividend / divider;
    }

    int divisionInteger(int dividend, int divider){
        return dividend / divider;
    }

    int integerSubtraction(int minuend, int subtrahend){
        return minuend - subtrahend;
    }
}
